/*#############################################################################################################*/
void UserDisplay::coordinate_to_pixel_position(float coor_x, float coor_y, int &pixel_x, int &pixel_y)
{
    if (coor_x < 0)
        coor_x = 0;
    if (coor_x > 10)
        coor_x = 10;
    if (coor_y < 0)
        coor_y = 0;
    if (coor_y > 10)
        coor_y = 10;
    // X: từ 0–10 ánh xạ thành pixel từ 9–211
    pixel_x = round(9 + (coor_x / 10.0f) * (211 - 9));
    // Y: từ 0–10 ánh xạ thành pixel từ 221–19 (ngược trục y TFT)
    pixel_y = round(221 - (coor_y / 10.0f) * (221 - 19));
}


